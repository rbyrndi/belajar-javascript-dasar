// > lebih dari
let result = 10 > 10
console.log(result)

// kurang dari
result = 10 < 10
console.log(result)

// sama dengan
result = 10 == 10
console.log(result)

// tidak sama dengan
result = 10 != 10
console.log(result)

// sama dengan sama tipe
result = 10 === "10"
console.log(result)

// lebih dari sama dengan
result = 10 >= 10
console.log(result)

//kurang dari sama dengan
result = 10 <= 10
console.log(result)